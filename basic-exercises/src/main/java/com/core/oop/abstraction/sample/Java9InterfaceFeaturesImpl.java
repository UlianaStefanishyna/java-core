package com.core.oop.abstraction.sample;

public class Java9InterfaceFeaturesImpl implements Java9InterfaceFeatures {

    @Override
    public void mul(int a, int b) {
        System.out.print("Answer by Abstract method = ");
        System.out.println(a * b);
    }

    public static void main(String[] args) {
        Java9InterfaceFeatures in = new Java9InterfaceFeaturesImpl();
        in.mul(2, 3);
        in.add(6, 2);

        Java9InterfaceFeatures.mod(5, 3);
    }
}
